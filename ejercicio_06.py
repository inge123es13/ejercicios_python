#Calcular la media de tres numeros pedidos por teclado



print ('Calculando la media de tres numeros')
num_1= float(input("Ingresa el primer numero: "))
num_2= float(input("Ingresa el segundo numero: "))
num_3= float(input("Ingresa el tercer numero: "))
media = (num_1 + num_2 + num_3) /3  

print('La media es : ', media)


